
# criando uma lista vazia

lista = []

# append(elemento): insere um elemento ao final

lista.append(12)
lista.append(20)
lista.append(32)


# insert(indice, elemento): insere um elemento e um indice especifico

lista.insert(1, 17)
lista.insert(0, 28)

# extend(coleção): insere uma coleção de forma individual

lista.extend([23, 18, 29])

print(lista)